'''
1.세트1 | 세트2 -> 합집합
2.세트1 & 세트2 -> 교집합
3.세트1 - 세트2 -> 차집합
4.세트1 ^ 세트2 -> 대칭차집합
'''


#1.합집합
a = {1,2,3,4}
b = {3,4,5,6}
r = a|b
print(r)

#2.교집합
a = {1,2,3,4}
b = {3,4,5,6}
r = a&b
print(r)

#3.차집합
a = {1,2,3,4}
b = {3,4,5,6}
r = a-b
print(r)

#4.대칭차집합
a = {1,2,3,4}
b = {3,4,5,6}
r = a^b
print(r)


